AND CI.CLASS_NM='단과반';

4.
SELECT CI.CLASS_NM, G.STUDENT_NM 
FROM TB_CLASS_INFO CI, TB_GRADE G
WHERE CI.CLASS_CD = G.CLASS_CD
AND G.KOR=100;

5.
SELECT CI.CLASS_NM, G.STUDENT_NM
FROM TB_CLASS_INFO CI, TB_GRADE G
WHERE CI.CLASS_CD = G.CLASS_CD
