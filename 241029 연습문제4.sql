SELECT C.CUSTOMER_CD, C.CUSTOMER_NM, CASE C.MW_FLG WHEN 'M' THEN '남' WHEN 'W' THEN '여' END AS 성별, C.PHONE_NUMBER
FROM TB_CUSTOMER C, TB_POINT P
WHERE P.CUSTOMER_CD=C.CUSTOMER_CD
AND P.POINT_MEMO LIKE '냉장고%' AND C.TOTAL_POINT>=15000;

